#!/usr/bin/env python3
import argparse
import csv
import math
from collections import defaultdict

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt


JOINT_ORDER = [
    "shoulder_pan_joint",
    "shoulder_lift_joint",
    "elbow_joint",
    "wrist_1_joint",
    "wrist_2_joint",
    "wrist_3_joint",
]

COLORS = {
    "shoulder_pan_joint": "#1f77b4",
    "shoulder_lift_joint": "#ff7f0e",
    "elbow_joint": "#2ca02c",
    "wrist_1_joint": "#d62728",
    "wrist_2_joint": "#9467bd",
    "wrist_3_joint": "#8c564b",
}


def parse_args():
    parser = argparse.ArgumentParser(description="Plot UR5e trajectory tracking error CSV.")
    parser.add_argument(
        "csv_file",
        nargs="?",
        default="/tmp/ur5e_trajectory_log.csv",
        help="CSV file generated by trajectory_recorder.",
    )
    parser.add_argument(
        "--output",
        default="/tmp/ur5e_tracking_error.png",
        help="Output image path.",
    )
    parser.add_argument(
        "--full-duration",
        action="store_true",
        help="Show the full recorded duration instead of focusing on the active tracking window.",
    )
    parser.add_argument(
        "--active-threshold-mrad",
        type=float,
        default=0.5,
        help="Error threshold used to detect the active tracking window.",
    )
    return parser.parse_args()


def as_float(value):
    if value == "":
        return math.nan
    return float(value)


def joint_label(joint):
    return joint.replace("_joint", "").replace("_", " ")


def rms(values):
    return math.sqrt(sum(value * value for value in values) / len(values))


def ordered_joints(series):
    known = [joint for joint in JOINT_ORDER if joint in series]
    extra = sorted(joint for joint in series if joint not in JOINT_ORDER)
    return known + extra


def main():
    args = parse_args()
    series = defaultdict(lambda: {"t": [], "error": []})

    with open(args.csv_file, newline="") as csv_file:
        reader = csv.DictReader(csv_file)
        for row in reader:
            if row["source"] != "controller_state":
                continue
            error = as_float(row["error_position"])
            if math.isnan(error):
                continue
            joint = row["joint"]
            series[joint]["t"].append(as_float(row["stamp_sec"]))
            series[joint]["error"].append(error)

    if not series:
        raise SystemExit("No controller_state error samples found in CSV.")

    first_time = min(values["t"][0] for values in series.values() if values["t"])
    joints = ordered_joints(series)
    relative = {}
    stats = {}
    for joint in joints:
        errors_rad = series[joint]["error"]
        relative[joint] = [stamp - first_time for stamp in series[joint]["t"]]
        stats[joint] = {
            "rms_mrad": rms(errors_rad) * 1000.0,
            "max_mrad": max(abs(value) for value in errors_rad) * 1000.0,
            "final_mrad": abs(errors_rad[-1]) * 1000.0,
        }

    xlim = None
    if not args.full_duration:
        active_times = []
        threshold_rad = args.active_threshold_mrad / 1000.0
        for joint in joints:
            for time_sec, error in zip(relative[joint], series[joint]["error"]):
                if abs(error) >= threshold_rad:
                    active_times.append(time_sec)
        if active_times:
            start = max(0.0, min(active_times) - 1.0)
            end = max(active_times) + 1.0
            if end > start:
                xlim = (start, end)

    try:
        plt.style.use("seaborn-v0_8-whitegrid")
    except OSError:
        plt.style.use("seaborn-whitegrid")
    fig = plt.figure(figsize=(13.5, 10.2), constrained_layout=True)
    grid = fig.add_gridspec(3, 3, height_ratios=[1.25, 1.0, 1.0])

    ax_overview = fig.add_subplot(grid[0, :])
    for joint in joints:
        values = series[joint]
        rel_time = relative[joint]
        errors_mrad = [error * 1000.0 for error in values["error"]]
        ax_overview.plot(
            rel_time,
            errors_mrad,
            label=joint_label(joint),
            color=COLORS.get(joint),
            linewidth=1.45,
            alpha=0.9,
        )

    ax_overview.axhline(0.0, color="#222222", linewidth=0.8, alpha=0.55)
    ax_overview.set_title("UR5e Cartesian Tracking Error - Six Joint Overview", fontsize=15, weight="bold")
    ax_overview.set_xlabel("Time (s)")
    ax_overview.set_ylabel("Position error (mrad)")
    if xlim is not None:
        ax_overview.set_xlim(*xlim)
    ax_overview.grid(True, alpha=0.28)
    ax_overview.legend(ncol=3, frameon=True, fontsize=9, loc="upper right")

    for index, joint in enumerate(joints[:6]):
        row = 1 + index // 3
        col = index % 3
        ax = fig.add_subplot(grid[row, col])
        values = series[joint]
        rel_time = relative[joint]
        errors_mrad = [error * 1000.0 for error in values["error"]]
        color = COLORS.get(joint, "#333333")

        ax.plot(rel_time, errors_mrad, color=color, linewidth=1.35)
        ax.fill_between(rel_time, errors_mrad, 0.0, color=color, alpha=0.12)
        ax.axhline(0.0, color="#222222", linewidth=0.7, alpha=0.5)
        ax.set_title(joint_label(joint), fontsize=11, weight="bold")
        ax.set_xlabel("Time (s)")
        ax.set_ylabel("Error (mrad)")
        if xlim is not None:
            ax.set_xlim(*xlim)
        ax.grid(True, alpha=0.25)
        ax.text(
            0.02,
            0.95,
            f"RMS {stats[joint]['rms_mrad']:.3f} mrad\n"
            f"Max {stats[joint]['max_mrad']:.3f} mrad\n"
            f"Final {stats[joint]['final_mrad']:.3f} mrad",
            transform=ax.transAxes,
            va="top",
            ha="left",
            fontsize=9,
            bbox={
                "boxstyle": "round,pad=0.32",
                "facecolor": "white",
                "edgecolor": "#c8c8c8",
                "alpha": 0.88,
            },
        )

    fig.suptitle(
        "Desired vs Actual Joint Tracking Error Recorded from joint_trajectory_controller",
        fontsize=12,
        y=1.02,
    )
    fig.savefig(args.output, dpi=220, bbox_inches="tight")
    print(f"Saved plot to {args.output}")


if __name__ == "__main__":
    main()
